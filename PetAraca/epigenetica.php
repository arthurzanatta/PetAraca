                <h2 align="center">Veja como o ambiente em volta de seu animal pode influenciar na epigenética!</h2></br>
                    <p align="justify">&nbsp;Segundo dados do Instituto Brasileiro de Geografia e Estatística (IBGE), existem no Brasil
                    cerca de 52,2 milhões de cães e 22,1 milhões de gatos, sendo o nosso país o quarto maior do mundo em
                    população total de animais de estimação. A população em média de cães e gatos vacinados na cidade de
                    Araçatuba corresponde a 22.964 e 3.127 respectivamente, totalizando 26.091 animais. Vale lembrar que,
                    mesmo já imunizados, estes devem permanecer sobre constantes cuidados, como banhos, tosas,
                    vermifugações e nutrição adequada. Além disso, é crucial que disponibilizemos tempo e atenção para os
                    nossos mascotes, cuidando com o amor e carinho que eles merecem.</p>
                    <p align="justify">&nbsp;Todos esses cuidados somados ao ambiente em que esse animal está exposto (uma situação de
                    estresse ou conforto), influenciam diretamente no crescimento e desenvolvimento corporal, além da parte
                    reprodutiva. Estes fatores exógenos podem afetar inclusive a expressão de determinados genes, vindo até
                    a silenciá-los. Isto é o que chamamos de Epigenética.</p>
                    <p align="justify">&nbsp;O termo epigenética foi usado pela primeira vez por em 1940, por Conrad Waddington para
                    descrever as interações de genes com o meio ambiente e os caminhos que cada célula pode tomar durante o
                    desenvolvimento. Epigenética significa “o que está acima” ou “em adição” à informação genética
                    codificada no DNA, ou seja, é um ramo da genética que estuda os mecanismos que promovem a regulação da
                    expressão de genes a nível da transcrição, por meio das alterações químicas no DNA e na cromatina
                    (Feinberg; Tycko, 2004; Richards, 2006; Talbert; Henikoff, 2006). Essas alterações podem modificar ou
                    não o fenótipo do animal, sem que ocorra alteração na sequência do código genético
                    (Gibbs, 2007; Jirtle; Skinner, 2007; Waggoner, 2007).</p>
                    <p align="justify">&nbsp;As alterações epigenéticas são adquiridas e estabelecidas ao longo da vida de um animal e
                    podem ser modificadas pelo ambiente. Experimentos recentes na área de epigenética sugerem que o meio
                    ambiente em que um ser vivo é inserido pode ser capaz de transmitir partículas elementares para a linha
                    germinativa, sendo a metilação do DNA a forma mais comum desse tipo de modificação adquirida e
                    transmissível (Tollefsbol et al., 2011).</p>
                    <p align="justify">&nbsp;Ao buscar sites de relacionamentos para nossos animais de estimação, com o intuito de
                    encontrar um companheiro para o seu animal, deve-se buscar um formulário completo de dados do animal,
                    como características físicas, vacinação, idade, raça, temperamento, comportamento e histórico
                    reprodutivo. Todas essas informações devem ser avaliadas no momento da escolha do parceiro, para uma
                    possível gestação. </p>
                    <p align="justify">&nbsp;A alimentação adequada dos animais de estimação é um fator que pode contribuir na epigenética,
                    ou seja, tudo o que for fornecido para essa mãe no seu período gestacional poderá influenciar nas
                    características fenotípicas dos filhotes.  Para isso é necessário o fornecimento de uma ração adequada
                    e balanceada, assim como a ingestão de líquidos.  Além disso, se você pretende que seu animal de
                    estimação reproduza-se, é fundamental que você proporcione bem-estar, como, por exemplo, conforto
                    térmico adequado, tosa bem feita e ambiente confortável. Desta maneira, evitaremos que esse animal
                    passe por situações de estresse e, consequentemente, esteja exposto a alterações na expressão de genes
                    que podem ser transmitidos as próximas gerações.</p>
