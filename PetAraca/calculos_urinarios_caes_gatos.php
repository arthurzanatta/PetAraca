                <h2 align="center">Cálculos urinários em cães e gatos</h2></br>
                    <p align="justify">&nbsp;Cálculos urinários são estruturas, também chamadas de urolitíases ou “pedras”, que podem se alojar no sistema urinário
dos animais. Estas podem bloquear a passagem da urina levando a diversos problemas para a saúde dos pets. Um dos locais mais comuns de ocorrência de cálculos é a
bexiga, mas eles também podem estar presentes em rins, ureteres e uretra.</p>
                    <h3>Como forma-se um cálculo urinário?</h3>
                    <p align="justify">&nbsp;Alterações na composição da urina podem levar a uma hipersaturação de substâncias que vão se agregando, formando os
cristais. As “pedras” se formam quando cristais microscópicos presentes na urina se agregam, dando origem a uma estrutura maior (o cálculo).</p>
                    <h3>Quais as principais causas?</h3>
                    <p align="justify">&nbsp;Predisposição racial (dálmata, shih-tzu, lhasa apso, yorkshire, etc são exemplos de raças que tendem a apresentar uma
maior incidência para este problema), dietas desbalanceadas, infecções do trato urinário e alterações anatômicas do trato urinário podem levar á formação de
urólitos. </p>
                    <h3>Como identificar se meu animal está com este problema?</h3>
                    <p align="justify">&nbsp;Os sintomas mais comuns incluem a dificuldade em urinar, muitas vezes o animal faz grande esforço, porém só consegue
urinar poucas gotas ou não urina; dor abdominal; urina com sangue; urina com presença de cristais; falta de apetite; apatia e vômito podendo evoluir para casos
graves e até mesmo óbito caso o animalzinho não receba atendimento de um profissional veterinário.</p>
                    <h3>Como funciona o tratamento?</h3>
                    <p align="justify">&nbsp;O tratamento baseia-se em desobstruir o fluxo de urina e restabelecer as funções orgânicas do paciente. Em alguns
casos o tratamento pode ser feito clinicamente, com troca de dieta, desobstrução por sondagem, medicamentos e manejo adequado. Na maioria das situações o uso da
terapia cirúrgica se torna necessária.</p>
                    <h3>Como posso evitar que meu animal apresente urolitíases?</h3>
                    <p align="justify">&nbsp;O aumento na ingestão de água eleva a diluição da urina sendo um dos pontos-chave na prevenção das urolitíases.
Fornecer dietas úmidas e água fresca de boa qualidade favorece o bom funcionamento do trato urinário. Evitar dietas desbalanceadas, comidas caseiras e excesso de
petiscos. Levar seu animalzinho a check-ups periódicos com um veterinário de sua confiança permite realizar exames para verificar qualquer alteração importante,
além de receber orientações de como evitar esse e muitos outros problemas de saúde.</p>
