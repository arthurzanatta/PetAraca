                <h2 align="center">Úlcera de córnea em cães e gatos</h2></br>
                    <p align="justify">&nbsp;Os olhos dos cães e gatos são muito delicados, assim como os dos humanos, e qualquer agressão a eles pode causar
                    muita irritação. Uma das causas mais comuns de dor ocular nessas espécies é a ceratite ulcerativa, também conhecida como úlcera de
                    córnea.</p>
                    <p align="justify">&nbsp;A córnea é a camada mais externa do olho, transparente, que permite a passagem de luz e protege as estruturas
                    internas do bulbo ocular. Um ferimento nesta estrutura, é chamado de úlcera e pode ser desde um arranhão, até lesões maiores e mais
                    graves.</p>
                    <p align="justify">&nbsp;Cães braquicefálicos (raças de focinho curto), como o shih-tzu, lhasa-apso, pug e bulldogs, são frequentemente
                    acometidos por essas lesões oculares, porque seus olhos são mais proeminentes e muito expostos. No entanto, as úlceras de córnea podem
                    ocorrer em qualquer outra raça e também nos gatos.</p>
                    <p align="justify">&nbsp;É sempre muito importante diagnosticar qual fator deu origem a lesão corneana a fim de evitar que ela ocorra
                    novamente no futuro. Podem ser citadas como causas comuns, os traumas externos, brigas, arranhaduras por outros animais e autotraumatismos.
                    Olho seco (deficiência da quantidade e/ou qualidade da lágrima), alterações palpebrais, cílios e pelos mal posicionados também podem gerar
                    ceratite ulcerativa.</p>
                    <p align="justify">&nbsp;Os sinais clínicos geralmente são dor, irritação ocular, sensibilidade a luz, animal com o olho sempre fechado,
                    secreção ocular e lacrimejamento. O diagnóstico precoce realizado por um médico veterinário através de exames oftálmicos específicos é de
                    extrema importância e quanto antes se instituir o tratamento, maiores são as chances de cicatrização.</p>
                    <p align="justify">&nbsp;O tratamento geralmente é realizado com colírios, ou pomadas oftálmicas e medicações sistêmicas. Porém alguns
                    casos mais graves e com complicações necessitam de intervenção cirúrgica.</p>
                    <p align="justify">&nbsp;Embora as úlceras de córnea possam aparentar ser simples, são um problema grave, que podem levar a perda da visão,
                    ou até mesmo do olho do animal. Caso seu cão, ou gato apresentem qualquer sinal de desconforto ocular, procure um profissional da área o
                    mais rápido possível.</p>
